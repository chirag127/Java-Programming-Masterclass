/*
Starting out with Expressions

This program demonstrates the use of expressions.
*/

class StartingoutwithExpressions {
    public static void main(String[] args) {
        System.out.println("This program demonstrates the use of expressions.");

        int myFirstNumber = 5 + 10;

        System.out.println(myFirstNumber);

        int mySecondNumber = ((myFirstNumber * 2) + 10);

        System.out.println(mySecondNumber);

    }
}