/** The POJO
 *
 * The plain old java object is a class that has no methods or
 * instance variables. It is used to store data.
 */

public class ThePOJO {

    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}

/** Output:
 *
 */

